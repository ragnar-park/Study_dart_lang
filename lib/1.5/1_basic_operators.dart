void main() {
  double number = 2;

  print(number + 2); // 4.0
  print(number - 2); // 0.0
  print(number * 2); // 4.0
  print(number / 2); // 1.0 몫
  print(number % 2); // 2.0 나머지

  // 단항 연산
  number++; // 3.0
  number--; // 2.0
  number += 2; // 4.0
  number -= 2; // 0.0
  number *= 2; // 4.0
  number /= 2; // 1.0

  // 다트 언어는 일반적으로 다른 언어에서 사용하는 기본 산수 기능 제공
}